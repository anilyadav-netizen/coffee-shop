// ========== HELPER FUNCTIONS ==========

// Get all gallery items
export const getGalleryItems = () => {
    return GALLERY_ITEMS;
};

// Get items by category
export const getGalleryItemsByCategory = (categoryId) => {
    return GALLERY_ITEMS.filter(item => item.category === categoryId);
};

// Get featured items
export const getFeaturedGalleryItems = () => {
    return GALLERY_ITEMS.filter(item => item.isFeatured);
};

// Get popular items (sorted by likes)
export const getPopularGalleryItems = () => {
    return [...GALLERY_ITEMS].sort((a, b) => b.likes - a.likes);
};

// Get random gallery items (for featured section)
export const getRandomGalleryItems = (count = 6) => {
    const shuffled = [...GALLERY_ITEMS].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
};

// Get gallery category by ID
export const getGalleryCategoryById = (categoryId) => {
    return GALLERY_CATEGORIES.find(cat => cat.id === categoryId);
};

// Get items count by category
export const getGalleryItemCount = (categoryId) => {
    return GALLERY_ITEMS.filter(item => item.category === categoryId).length;
};

// Get total gallery items count
export const getTotalGalleryItems = () => {
    return GALLERY_ITEMS.length;
};

// Get all tags
export const getAllTags = () => {
    const tagsSet = new Set();
    GALLERY_ITEMS.forEach(item => {
        item.tags.forEach(tag => tagsSet.add(tag));
    });
    return Array.from(tagsSet);
};

// Search gallery items
export const searchGalleryItems = (query) => {
    const searchTerm = query.toLowerCase().trim();
    return GALLERY_ITEMS.filter(item => 
        item.title.toLowerCase().includes(searchTerm) ||
        item.description.toLowerCase().includes(searchTerm) ||
        item.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
        item.category.toLowerCase().includes(searchTerm)
    );
};